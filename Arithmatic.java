public class Arithmatic {
    public static void main(String[] args) {
        System.out.println("..........arithmatic operations........");
        int a =10;
        int b =4;
        System.out.println(a+b);  //14
        System.out.println(a-b);  //6
        System.out.println(a*b);   //40
        System.out.println(a/b);   //2
        System.out.println(a%b);  //2  it is the remainder
        double x=20;
        double y=6;
        System.out.println(x+y);//26.0
        System.out.println(x-y);  //14.0
        System.out.println(x*y);  //120.0
        System.out.println(x/y);  //3.3333333333333335
        System.out.println(x%y);  //2.0

    }
}
